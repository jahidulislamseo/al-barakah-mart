export async function GET() {
    try {
        // 1. Seed Categories
        for (const category of categories) {
            await prisma.category.upsert({
                where: { slug: category.slug },
                update: {},
                create: category
            });
        }

        // 2. Seed Products
        let createdCount = 0;
        for (const product of productsWithSEO) {
            const category = await prisma.category.findUnique({
                where: { slug: product.categorySlug }
            });

            if (category) {
                await prisma.product.upsert({
                    where: { slug: product.slug },
                    update: {
                        description: product.description,
                        price: product.price,
                        originalPrice: product.originalPrice,
                        images: (product as any).images,
                        rating: product.rating,
                        reviewsCount: product.reviewsCount
                    } as any,
                    create: {
                        title: product.title,
                        slug: product.slug,
                        description: product.description,
                        price: product.price,
                        originalPrice: product.originalPrice,
                        image: product.image,
                        images: (product as any).images,
                        rating: product.rating,
                        reviewsCount: product.reviewsCount,
                        inStock: product.inStock,
                        isNew: product.isNew,
                        isBestSeller: product.isBestSeller,
                        categoryId: category.id
                    } as any
                });
                createdCount++;
            }
        }

        return NextResponse.json({
            success: true,
            message: `Successfully seeded ${createdCount} products and ${categories.length} categories!`,
            db: process.env.DATABASE_URL?.split('@')[1] || 'Hidden URL' // Quick check to see which DB was hit
        });

    } catch (error: any) {
        console.error('Seeding Error:', error);
        return NextResponse.json({
            success: false,
            error: error.message
        }, { status: 500 });
    }
}
